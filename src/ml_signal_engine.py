def get_latest_signals(n_signals=20):
    """Retrieve latest N signals for all tickers."""
    if not SIGNALS_PATH.exists():
        log_error(f"Signals file not found: {SIGNALS_PATH}")
        return None
    
    conn = duckdb.connect()
    query = f"""
        SELECT * FROM read_parquet('{SIGNALS_PATH}')
        ORDER BY report_date DESC, ticker
        LIMIT {n_signals}
    """
    result = conn.execute(query).df()
    conn.close()
    return result

def get_signals_by_ticker(ticker, limit=50):
    """Get all signals for a specific ticker."""
    if not SIGNALS_PATH.exists():
        log_error(f"Signals file not found: {SIGNALS_PATH}")
        return None
    
    conn = duckdb.connect()
    query = f"""
        SELECT * FROM read_parquet('{SIGNALS_PATH}')
        WHERE ticker = '{ticker}'
        ORDER BY report_date DESC
        LIMIT {limit}
    """
    result = conn.execute(query).df()
    conn.close()
    return result

def get_signal_statistics():
    """Get aggregated signal statistics."""
    if not SIGNALS_PATH.exists():
        log_error(f"Signals file not found: {SIGNALS_PATH}")
        return None
    
    conn = duckdb.connect()
    query = f"""
        SELECT 
            ticker,
            COUNT(*) as total_signals,
            SUM(CASE WHEN ml_signal = 1 THEN 1 ELSE 0 END) as buy_signals,
            SUM(CASE WHEN ml_signal = -1 THEN 1 ELSE 0 END) as sell_signals,
            SUM(CASE WHEN ml_signal = 0 THEN 1 ELSE 0 END) as hold_signals,
            AVG(ml_probability) as avg_probability,
            MAX(report_date) as latest_date
        FROM read_parquet('{SIGNALS_PATH}')
        GROUP BY ticker
        ORDER BY ticker
    """
    result = conn.execute(query).df()
    conn.close()
    return result

def get_model_performance():
    """Get model performance metrics for all tickers."""
    if not PERFORMANCE_PATH.exists():
        log_error(f"Performance metrics file not found: {PERFORMANCE_PATH}")
        return None
    
    conn = duckdb.connect()
    df = conn.execute(f"SELECT * FROM read_parquet('{PERFORMANCE_PATH}') ORDER BY ticker").df()
    conn.close()
    return df

def get_model_performance_by_ticker(ticker):
    """Get performance metrics for specific ticker."""
    if not PERFORMANCE_PATH.exists():
        log_error(f"Performance metrics file not found: {PERFORMANCE_PATH}")
        return None
    
    conn = duckdb.connect()
    query = f"""
        SELECT * FROM read_parquet('{PERFORMANCE_PATH}')
        WHERE ticker = '{ticker}'
    """
    result = conn.execute(query).df()
    conn.close()
    
    if len(result) == 0:
        log_warning(f"No performance metrics found for {ticker}")
        return None
    
    return result.iloc[0]

def display_model_performance_summary():
    """Display formatted model performance summary."""
    df = get_model_performance()
    if df is None or len(df) == 0:
        print("No performance metrics available")
        return
    
    print("\n" + "="*100)
    print("MODEL PERFORMANCE SUMMARY (All Tickers)")
    print("="*100)
    
    # Summary statistics
    print("\nAGGREGATED METRICS:")
    print(f"  Total Tickers:    {len(df)}")
    print(f"  Accuracy:         {df['accuracy'].mean():.4f} ± {df['accuracy'].std():.4f}")
    print(f"  Precision:        {df['precision'].mean():.4f} ± {df['precision'].std():.4f}")
    print(f"  Recall:           {df['recall'].mean():.4f} ± {df['recall'].std():.4f}")
    print(f"  F1-Score:         {df['f1_score'].mean():.4f} ± {df['f1_score'].std():.4f}")
    print(f"  ROC-AUC:          {df['roc_auc'].mean():.4f} ± {df['roc_auc'].std():.4f}")
    
    # Top performers
    print("\nTOP 10 PERFORMERS (by ROC-AUC):")
    top_df = df.nlargest(10, 'roc_auc')[['ticker', 'accuracy', 'precision', 'recall', 'f1_score', 'roc_auc', 'total_samples']]
    print(top_df.to_string(index=False))
    
