# Model hyperparameters
XGB_PARAMS = {
    'max_depth': 5,
    'n_estimators': 100,
    'learning_rate': 0.1,
    'subsample': 0.8,
    'colsample_bytree': 0.8,
    'random_state': 42,
    'verbosity': 0
}

# Signal generation thresholds
BULLISH_THRESHOLD = 0.60  # Confidence threshold for buy signal
BEARISH_THRESHOLD = 0.40  # Confidence threshold for sell signal
TARGET_FORWARD_DAYS = 5    # Predict price movement N days ahead

# Feature selection
META_COLS = ['ticker', 'report_date', 'close', 'volume']
DROP_COLS = ['open', 'high', 'low', 'adj_close']  # Raw OHLCV columns

