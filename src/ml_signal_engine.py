def train_and_predict_sector(sector_df, feature_cols, sector_name):
    """
    Train XGBoost model with walk-forward validation for entire sector.
    One model for all stocks in the sector.
    Calculates performance metrics: accuracy, precision, recall, F1, ROC-AUC.
    Returns tuple: (signals_df, performance_metrics_dict)
    """
    try:
        X, y, df = prepare_training_data(sector_df, feature_cols)
        
        if len(X) < 100:
            log_warning(f"  {sector_name}: Insufficient samples ({len(X)} < 100), skipping")
            return None, None
        
        # Diagnostic: Check feature-target correlation (only for first sector to avoid spam)
        if sector_name == sorted(sector_df['sector'].unique())[0]:
            feature_target_corr = np.abs([np.corrcoef(X[:, i], y)[0, 1] for i in range(min(10, X.shape[1]))])
            max_corr = np.max(feature_target_corr)
            log_info(f"  Max feature-target correlation: {max_corr:.4f} (>0.1 needed for predictive power)")
            if max_corr < 0.05:
                log_warning(
                    "  ⚠️  Very weak feature-target correlation (<0.05).\n"
                    "     Technical indicators are not predictive of 10-day returns.\n"
                    "     Consider using rule-based signal_engine.py instead of ML."
                )
        
        # Walk-forward validation (time series split)
        n_splits = min(3, len(X) // 100)  # Reduced from 5 to 3 for speed
        tscv = TimeSeriesSplit(n_splits=n_splits)
        
        all_signals = []
        all_proba = []
        all_actuals = []
        indices = []
        
        # Metrics aggregation across all splits
        all_accuracies = []
        all_precisions = []
        all_recalls = []
        all_f1s = []
        all_roc_aucs = []
        
        for fold, (train_idx, test_idx) in enumerate(tscv.split(X), 1):
            X_train, X_test = X[train_idx], X[test_idx]
            y_train, y_test = y[train_idx], y[test_idx]
            
            # Scale features
            scaler = StandardScaler()
            X_train_scaled = scaler.fit_transform(X_train)
            X_test_scaled = scaler.transform(X_test)
            
            # Train model with class weight balancing for imbalanced targets
            n_neg = np.sum(y_train == 0)
            n_pos = np.sum(y_train == 1)
            scale_pos_weight = max(n_neg / (n_pos + 1), 0.5)  # Avoid extreme imbalance
            
            params = XGB_PARAMS.copy()
            params['scale_pos_weight'] = scale_pos_weight
            
            model = xgb.XGBClassifier(**params)
            model.fit(X_train_scaled, y_train, verbose=False)
            
            # Generate predictions
            proba = model.predict_proba(X_test_scaled)  # [prob_bearish, prob_bullish]
            prob_bullish = proba[:, 1]
            preds = model.predict(X_test_scaled)
            
            # Generate signals: 1 (buy), 0 (hold), -1 (sell)
            signals = np.where(
                prob_bullish > BULLISH_THRESHOLD, 
                1, 
                np.where(prob_bullish < BEARISH_THRESHOLD, -1, 0)
            )
            
            # Calculate metrics
            accuracy = accuracy_score(y_test, preds)
            precision = precision_score(y_test, preds, zero_division=0)
            recall = recall_score(y_test, preds, zero_division=0)
            f1 = f1_score(y_test, preds, zero_division=0)
            roc_auc = roc_auc_score(y_test, prob_bullish)
            
            all_accuracies.append(accuracy)
            all_precisions.append(precision)
            all_recalls.append(recall)
            all_f1s.append(f1)
            all_roc_aucs.append(roc_auc)
            
            all_signals.extend(signals)
            all_proba.extend(prob_bullish)
            all_actuals.extend(y_test)
            indices.extend(test_idx)
        
        # Rebuild result dataframe with signals
        result = df.iloc[indices].copy()
        result['ml_signal'] = all_signals
        result['ml_probability'] = all_proba
        result = result[['ticker', 'report_date', 'close', 'ml_signal', 'ml_probability', 'target']]
        
        # Aggregate performance metrics
        performance = {
            'sector': sector_name,
            'total_samples': len(X),
            'n_folds': len(all_accuracies),
            'accuracy': np.mean(all_accuracies),
            'accuracy_std': np.std(all_accuracies),
            'precision': np.mean(all_precisions),
            'precision_std': np.std(all_precisions),
            'recall': np.mean(all_recalls),
            'recall_std': np.std(all_recalls),
            'f1_score': np.mean(all_f1s),
            'f1_score_std': np.std(all_f1s),
            'roc_auc': np.mean(all_roc_aucs),
            'roc_auc_std': np.std(all_roc_aucs),
            'bullish_signals': int(np.sum(np.array(all_signals) == 1)),
            'bearish_signals': int(np.sum(np.array(all_signals) == -1)),
            'hold_signals': int(np.sum(np.array(all_signals) == 0)),
        }
        
        return result, performance
    
    except Exception as e:
        log_error(f"  {sector_name}: {str(e)}")
        return None, None

def generate_ml_signals(validate_features_flag=True):
    """
    Generate ML-based buy/sell signals using SECTOR-BASED CLUSTERING.
    
    Process:
    1. Load features from parquet (created by feature_engine)
    2. Load sector information from tickers.csv
    3. Create binary classification targets (10-day forward return)
    4. Train ONE XGBoost model PER SECTOR (not per ticker)
    5. Generate signals based on predicted probabilities
    6. Save results to parquet
    
    Runtime: ~10-15 minutes (vs 1-2 hours with per-ticker models)
    
    Returns:
        DataFrame with columns: [ticker, report_date, close, ml_signal, ml_probability, target]
    """
    log_section("ML Signal Generation (Sector-Based)")
    
    # -------------------------
    # LOAD & VALIDATE FEATURES
    # -------------------------
    try:
        df_feat = load_features_from_parquet()
    except FileNotFoundError as e:
        log_error(str(e))
        return None
    
    if validate_features_flag:
        df_feat = validate_features(df_feat)
    
    n_tickers = df_feat['ticker'].nunique()
    log_info(f"Processing {n_tickers} tickers with {len(df_feat):,} rows")
    
    # -------------------------
    # LOAD SECTOR INFORMATION
    # -------------------------
    ticker_sectors = load_sectors_from_csv()
    
    if ticker_sectors is None:
        log_warning("Sector CSV not available, fetching from yfinance (slow)...")
        ticker_sectors = {}
        for ticker in df_feat['ticker'].unique():
            ticker_sectors[ticker] = get_sector_for_ticker(ticker)
            time.sleep(0.2)  # Rate limit
    
    # Add sector column
    df_feat['sector'] = df_feat['ticker'].map(ticker_sectors).fillna('Unknown')
    
    n_sectors = df_feat['sector'].nunique()
    sector_dist = df_feat['ticker'].groupby(df_feat['sector']).nunique()
    log_info(f"Grouped into {n_sectors} sectors")
    for sector, count in sector_dist.items():
        log_info(f"  {sector}: {count} tickers")
    
    # -------------------------
    # CREATE TARGETS (10-day forward prediction)
    # -------------------------
    log_info("Creating target variables (10-day forward returns)...")
    df_feat = create_target_variable(df_feat, TARGET_FORWARD_DAYS)
    
    # -------------------------
    # GET FEATURE COLUMNS & VALIDATE TARGET DISTRIBUTION
    # -------------------------
    feature_cols = get_feature_columns(df_feat)
    log_info(f"Using {len(feature_cols)} features for training")
    
    # Check target class balance
    target_dist = df_feat['target'].value_counts(normalize=True)
    log_info(f"Target distribution: Bearish (0): {target_dist.get(0, 0):.1%}, Bullish (1): {target_dist.get(1, 0):.1%}")
    
    # Warning if target is nearly 50/50 (suggests random/unpredictable)
    if 0.48 <= target_dist.get(1, 0.5) <= 0.52:
        log_warning(
            "⚠️  Target distribution is ~50/50, suggesting 10-day forward returns are random/unpredictable.\n"
            "    Technical indicators may not have predictive power for short-term price movements.\n"
            "    Consider: (1) Longer horizons (20-30 days), (2) Relative returns vs SPY, or (3) Fundamental features."
        )
    
    # -------------------------
    # TRAIN & GENERATE SIGNALS (PER SECTOR, NOT PER TICKER)
    # -------------------------
    log_info(f"Training sector models and generating signals...")
    
    all_signals = []
    all_performance = []
    successful_sectors = []
    failed_sectors = []
    
    process_start = time.perf_counter()
    
    for i, sector in enumerate(sorted(df_feat['sector'].unique()), 1):
        sector_df = df_feat[df_feat['sector'] == sector].sort_values('report_date').reset_index(drop=True)
        sector_tickers = sector_df['ticker'].nunique()
        
        log_info(f"  [{i}/{n_sectors}] {sector} ({len(sector_df)} rows, {sector_tickers} tickers)")
        
        result, perf = train_and_predict_sector(sector_df, feature_cols, sector)
        if result is not None:
            all_signals.append(result)
            all_performance.append(perf)
            successful_sectors.append(sector)
            # Log sector-specific metrics
            log_info(f"      Accuracy: {perf['accuracy']:.3f} | F1: {perf['f1_score']:.3f} | ROC-AUC: {perf['roc_auc']:.3f}")
        else:
            failed_sectors.append(sector)
    
    process_time = time.perf_counter() - process_start
    log_info(f"Sector model training: {process_time:.1f}s ({n_sectors/max(process_time, 0.1):.1f} sectors/sec)")
    
    if not all_signals:
        log_error("No signals generated")
        return None
    
    # -------------------------
    # COMBINE RESULTS
    # -------------------------
    df_signals = pd.concat(all_signals, ignore_index=True).sort_values(['ticker', 'report_date'])
    
    log_info(f"Generated signals for {len(successful_sectors)} sectors")
    if failed_sectors:
        log_warning(f"Failed: {', '.join(failed_sectors)}")
    
    # -------------------------
    # SAVE SIGNALS TO PARQUET
    # -------------------------
    MODELS_PATH.mkdir(exist_ok=True)
    log_info(f"Saving signals to {SIGNALS_PATH.name}...")
    
    save_start = time.perf_counter()
    conn = duckdb.connect()
    conn.execute(f"COPY df_signals TO '{SIGNALS_PATH}' (FORMAT PARQUET, COMPRESSION 'snappy')")
    conn.close()
    save_time = time.perf_counter() - save_start
    
    file_size_mb = SIGNALS_PATH.stat().st_size / (1024 * 1024)
    log_info(f"Signals saved: {len(df_signals):,} rows, {file_size_mb:.1f} MB in {save_time:.1f}s")
    
    # -------------------------
    # SAVE MODEL PERFORMANCE METRICS TO PARQUET
    # -------------------------
    if all_performance:
        log_info(f"Saving sector model performance metrics to {PERFORMANCE_PATH.name}...")
        df_perf = pd.DataFrame(all_performance)
        
        perf_start = time.perf_counter()
        conn = duckdb.connect()
        conn.execute(f"COPY df_perf TO '{PERFORMANCE_PATH}' (FORMAT PARQUET, COMPRESSION 'snappy')")
        conn.close()
        perf_time = time.perf_counter() - perf_start
        
        log_info(f"Performance metrics saved in {perf_time:.1f}s")
        
        # Log aggregated performance stats
        log_info("Aggregated Sector Model Performance:")
        log_info(f"  Accuracy:  {df_perf['accuracy'].mean():.3f} ± {df_perf['accuracy'].std():.3f}")
        log_info(f"  Precision: {df_perf['precision'].mean():.3f} ± {df_perf['precision'].std():.3f}")
        log_info(f"  Recall:    {df_perf['recall'].mean():.3f} ± {df_perf['recall'].std():.3f}")
        log_info(f"  F1-Score:  {df_perf['f1_score'].mean():.3f} ± {df_perf['f1_score'].std():.3f}")
        log_info(f"  ROC-AUC:   {df_perf['roc_auc'].mean():.3f} ± {df_perf['roc_auc'].std():.3f}")
    
    # -------------------------
    # SIGNAL STATISTICS
    # -------------------------
    signal_dist = df_signals['ml_signal'].value_counts()
    log_metrics({
        "Total Signals": len(df_signals),
        "Buy (1)": signal_dist.get(1, 0),
        "Hold (0)": signal_dist.get(0, 0),
        "Sell (-1)": signal_dist.get(-1, 0),
        "Avg Probability": f"{df_signals['ml_probability'].mean():.3f}",
        "Sectors": len(successful_sectors),
        "Tickers": df_signals['ticker'].nunique()
    })
    
    return df_signals

