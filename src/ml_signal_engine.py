def load_features_from_parquet():
    """
    Load computed features from parquet file generated by feature_engine.
    Returns DataFrame with features ready for ML training.
    """
    if not FEATURES_PATH.exists():
        raise FileNotFoundError(
            f"Feature parquet not found: {FEATURES_PATH}\n"
            f"Run feature_engine.py first to generate features."
        )
    
    log_info(f"Loading features from {FEATURES_PATH.name}...")
    load_start = time.perf_counter()
    
    # Load via DuckDB for efficiency
    conn = duckdb.connect()
    df = conn.execute(f"SELECT * FROM read_parquet('{FEATURES_PATH}')").df()
    conn.close()
    
    load_time = time.perf_counter() - load_start
    log_info(f"Loaded {len(df):,} rows, {len(df.columns)} columns in {load_time:.1f}s")
    
    return df

def validate_features(df):
    """Validate feature DataFrame for ML pipeline."""
    # Check required columns
    required = ['ticker', 'report_date', 'close', 'volume']
    missing = [col for col in required if col not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns: {missing}")
    
    # Check data types
    if df['close'].dtype in ['object', 'string']:
        df['close'] = pd.to_numeric(df['close'], errors='coerce')
    
    # Drop rows with NaN close/volume
    initial_rows = len(df)
    df = df.dropna(subset=['close', 'volume'])
    dropped = initial_rows - len(df)
    if dropped > 0:
        log_warning(f"Dropped {dropped} rows with missing close/volume")
    
    return df

def create_target_variable(df, forward_days=TARGET_FORWARD_DAYS):
    """
    Create binary classification target: 1 if price rises in next N days, else 0.
    Prevents look-ahead bias by only creating targets where future data exists.
    Uses shift() for robust forward-looking window.
    """
    # Ensure data is sorted by ticker and date
    df = df.sort_values(['ticker', 'report_date']).reset_index(drop=True)
    
    # Create future price column (shifted by forward_days for each ticker)
    df['future_close'] = df.groupby('ticker')['close'].shift(-forward_days)
    
    # Create target: 1 if price rises, 0 otherwise
    df['target'] = (df['future_close'] > df['close']).astype(int)
    
    # Drop rows where we don't have future data
    initial = len(df)
    df = df.dropna(subset=['future_close']).reset_index(drop=True)
    dropped = initial - len(df)
    log_info(f"Created targets: {dropped:,} rows removed (not enough future data)")
    
    # Drop the temporary future_close column
    df = df.drop('future_close', axis=1)
    
    return df

def get_feature_columns(df):
    """Extract feature column names (exclude metadata and raw OHLCV)."""
    features = [col for col in df.columns 
                if col not in META_COLS + DROP_COLS + ['target']]
    return features

def prepare_training_data(df, feature_cols):
    """Prepare X, y, and handle NaN values in features."""
    # Drop NaN values
    initial = len(df)
    df = df.dropna(subset=feature_cols + ['target']).reset_index(drop=True)
    dropped = initial - len(df)
    if dropped > 0:
        log_warning(f"Dropped {dropped:,} rows with NaN features ({dropped/initial*100:.1f}%)")
    
    X = df[feature_cols].values
    y = df['target'].values
    
    return X, y, df

