def get_signal_statistics():
    """Get aggregated signal statistics."""
    if not SIGNALS_PATH.exists():
        log_error(f"Signals file not found: {SIGNALS_PATH}")
        return None
    
    conn = duckdb.connect()
    query = f"""
        SELECT 
            ticker,
            COUNT(*) as total_signals,
            SUM(CASE WHEN ml_signal = 1 THEN 1 ELSE 0 END) as buy_signals,
            SUM(CASE WHEN ml_signal = -1 THEN 1 ELSE 0 END) as sell_signals,
            SUM(CASE WHEN ml_signal = 0 THEN 1 ELSE 0 END) as hold_signals,
            AVG(ml_probability) as avg_probability,
            MAX(report_date) as latest_date
        FROM read_parquet('{SIGNALS_PATH}')
        GROUP BY ticker
        ORDER BY ticker
    """
    result = conn.execute(query).df()
    conn.close()
    return result

